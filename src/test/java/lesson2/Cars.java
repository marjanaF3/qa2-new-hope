package lesson2;

public class Cars {
    private String carModel;
    private int fuelConsuption;
    private int averageSpeed;
    private float fuelPrice;
    private int loadPrice;
    private int unloadPrice;

    private int distanceTogo;
    private int loadWeight;

    public int getLoadWeight() {
        return loadWeight;
    }

    public void setLoadWeight(int loadWeight) {
        this.loadWeight = loadWeight;
    }

    public int getDistanceTogo() {
        return distanceTogo;
    }

    public void setDistanceTogo(int distanceTogo) {
        this.distanceTogo = distanceTogo;
    }

    public String getCarModel() {
        return carModel;
    }

    public void setCarModel(String carModel) {
        this.carModel = carModel;
    }

    public int getFuelConsuption() {
        return fuelConsuption;
    }

    public void setFuelConsuption(int fuelConsuption) {
        this.fuelConsuption = fuelConsuption;
    }

    public int getAverageSpeed() {
        return averageSpeed;
    }

    public void setAverageSpeed(int averageSpeed) {
        this.averageSpeed = averageSpeed;
    }

    public double getFuelPrice() {
        return fuelPrice;
    }

    public void setFuelPrice(float fuelPrice) {
        this.fuelPrice = fuelPrice;
    }

    public int getLoadPrice() {
        return loadPrice;
    }

    public void setLoadPrice(int loadPrice) {
        this.loadPrice = loadPrice;
    }

    public int getUnloadPrice() {
        return unloadPrice;
    }

    public void setUnloadPrice(int unloadPrice) {
        this.unloadPrice = unloadPrice;
    }


}



